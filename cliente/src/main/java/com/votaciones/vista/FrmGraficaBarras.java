    @Override
    public void onCambioVotos(List<ProductoDTO> productos) {
        String[] etiquetas = productos.stream().map(ProductoDTO::getNombre).toArray(String[]::new);
        int[] valores = productos.stream().mapToInt(ProductoDTO::getVotos).toArray();
        graficaBarras.setDatos(etiquetas, valores);
        getContentPane().revalidate();
        getContentPane().repaint();
    }

 


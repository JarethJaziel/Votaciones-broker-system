    private void btnBuscarServicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarServicioActionPerformed
        // TODO add your handling code here:
        String[] columnas = {"Servicio", "Servidor"};
        DefaultTableModel modeloTabla = new DefaultTableModel(columnas, 0);
        
        Map<String, String> servicios = ctrlBroker.listarServicio(txtServicio.getText()
                                                                    .toLowerCase());

        for (Map.Entry<String, String> entry : servicios.entrySet()){
            Object[] data = {
                entry.getKey(),
                entry.getValue()
            };
            modeloTabla.addRow(data);
        }
        tbServicios.setModel(modeloTabla);
    }//GEN-LAST:event_btnBuscarServicioActionPerformed


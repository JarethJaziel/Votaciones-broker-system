    private void refrescarServicios() {
        String[] columnas = {"Servicio", "Servidor"};
        DefaultTableModel modeloTabla = new DefaultTableModel(columnas, 0);
        
        Map<String, String> servicios = ctrlBroker.listarServicios();

        for (Map.Entry<String, String> entry : servicios.entrySet()){
            Object[] data = {
                entry.getKey(),
                entry.getValue()
            };
            modeloTabla.addRow(data);
        }
        tbServicios.setModel(modeloTabla);
    }
